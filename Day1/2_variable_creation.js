// How to create the variable in the javascript?

// var
// let 
// const 


// using var keyword - old
var myName = "Dnyaneshwar";
console.log("my name is " + myName);

var myName = "Priya"; // re-declare

myName = "DD";
console.log("my name after " + myName);

// using let keyword - new
let firstName = "Dnyaneshwar";
let lastName = "Divekar";

let firstName = "Shyam"; // re-declare -not allowed

console.log(firstName + lastName);

firstName = "Ankul";
lastName ="Rathod";
console.log(firstName + " " +  lastName);

// using const keyword  - constants - we can't change the value

const email = "test@gmail.com";
console.log(email);

