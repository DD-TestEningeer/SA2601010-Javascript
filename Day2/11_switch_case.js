// switch case

// open browser based on the provided choice

// chrome, edge, firefox

let choice = "safari";

switch (choice) {
  case "chrome": {
    console.log("Chrome browser opened successfully");
    break;
  }

  case "edge": {
    console.log("Edge browser opened successfully");
    break;
  }

  case "firefox": {
    console.log("Firefox browser opened successfully");
    break;
  }

  default:
    console.log("Please select correct browser name - chrome, edge, firefox");
}
