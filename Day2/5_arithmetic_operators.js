// 2 variables 

let num1 = 10; 
let num2 = 5;

let result = 0;


result = num1 + num2;
console.log("Add operation : " + result); // 15

result = num1 - num2;
console.log("Sub operation : " + result); // 5

result = num1 * num2;
console.log("Mul operation : " + result); // 50

result = num1 / num2;
console.log("Div operation : " + result); // 2

result = num1 % num2;
console.log("Mod/ Remainder operation : " + result); // 0


